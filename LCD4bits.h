/***********************************************************************
 sends 4 bits data on LCD port according schématics used
*************************************************************************/
 void  Sdata(char);
/************************************************************************/



/***********************************************************************
initializes by instruction when interface is 4 bits long
void  lcd_setup(bit N) 
N = 1 :  2 lines, N = 0 : 1 line

*************************************************************************/
 void  lcd_setup(char);
/************************************************************************/


/***********************************************************************
sends char on display at the current cursor position
*************************************************************************/
 void  lcd_printChar(char);
/************************************************************************/


/***********************************************************************
sends string on display
*************************************************************************/
 void  lcd_print(const char*);   //pointeur d'une chaîne de caractères
/************************************************************************/


/***********************************************************************
sends single decimal value on display
*************************************************************************/
 void lcd_printDigit(char);
/************************************************************************/


/***********************************************************************
Places cursor at X, Y position. (0,0) is the left upside corner
*************************************************************************/
 void lcd_GotoXY(char, char);
/************************************************************************/

 

/***********************************************************************
Clears LCD display
*************************************************************************/
 void lcd_Clr(void);
/************************************************************************/


/***********************************************************************
Displays cursor
*************************************************************************/
 void lcd_CursorON(void);
/************************************************************************/



/***********************************************************************
Hides cursor
*************************************************************************/
 void lcd_CursorOFF(void);
/************************************************************************/


/***********************************************************************
Displays multi digit decimal value (16 bits max, 65535 max !)
*************************************************************************/
 void lcd_printNum(signed short);
/************************************************************************/
